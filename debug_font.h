static inline unsigned char *
FNT_bake_string_to_bmp(const char *text, int *out_w, int *out_h) {
  const int glyph_w = 8, glyph_h = 8;
  const int row_pad = 1;

  int max_cols = 0;
  int cols = 0, rows = 1;
  for (const char *p = text; *p; ++p) {
    if (*p == '\n') {
      if (cols > max_cols) max_cols = cols;
      cols = 0;
      rows++;
    } else if (*p == '\t') {
      cols += 1;
    } else {
      cols += 1;
    }
  }
  if (cols > max_cols) max_cols = cols;

  int W  = max_cols * glyph_w;
  int H  = rows * glyph_h + (rows - 1) * row_pad;
  *out_w = W;
  *out_h = H;

  size_t         total_bytes = (size_t)W * H * 3;
  unsigned char *out         = malloc(total_bytes);
  if (!out) return NULL;

  memset(out, 255, total_bytes);

  int cur_row = 0, cur_col = 0;
  for (const char *p = text; *p; ++p) {
    if (*p == '\n') {
      cur_row++;
      cur_col = 0;
    } else {
      int x0 = cur_col * glyph_w;
      int y0 = cur_row * (glyph_h + row_pad);

      unsigned char *dst = out + ((y0 * W + x0) * 3);
      unsigned char *glyph;

      if (*p == '\t') {
        glyph = NULL;
      } else {
        glyph = FNT_codepoint_to_bmp(*p);
      }

      if (glyph) {
        for (int yy = 0; yy < glyph_h; yy++) {
          unsigned char *g_row = glyph + yy * glyph_w * 3;
          unsigned char *d_row = dst + yy * W * 3;
          memcpy(d_row, g_row, glyph_w * 3);
        }
      }
      cur_col++;
    }
  }

  return out;
}

int write_bmp(
  const char          *filename,
  const unsigned char *data,
  int                  width,
  int                  height
) {
  FILE *f = fopen(filename, "wb");
  if (!f) return -1;

  int rowSize    = (width * 3 + 3) & ~3;
  int dataSize   = rowSize * height;
  int headerSize = sizeof(BITMAPFILEHEADER) + sizeof(BITMAPINFOHEADER);

  BITMAPFILEHEADER bfh = {
    .bfType      = 0x4D42, // 'BM'
    .bfSize      = headerSize + dataSize,
    .bfReserved1 = 0,
    .bfReserved2 = 0,
    .bfOffBits   = headerSize
  };

  BITMAPINFOHEADER bih = {
    .biSize          = sizeof(BITMAPINFOHEADER),
    .biWidth         = width,
    .biHeight        = height,
    .biPlanes        = 1,
    .biBitCount      = 24,
    .biCompression   = 0,
    .biSizeImage     = dataSize,
    .biXPelsPerMeter = 0,
    .biYPelsPerMeter = 0,
    .biClrUsed       = 0,
    .biClrImportant  = 0
  };

  fwrite(&bfh, sizeof bfh, 1, f);
  fwrite(&bih, sizeof bih, 1, f);

  unsigned char pad[3] = {0, 0, 0};
  for (int y = height - 1; y >= 0; --y) {
    const unsigned char *row = data + (size_t)y * width * 3;
    for (int x = 0; x < width; ++x) {
      unsigned char r        = row[x * 3 + 0];
      unsigned char g        = row[x * 3 + 1];
      unsigned char b        = row[x * 3 + 2];
      unsigned char pixel[3] = {b, g, r};
      fwrite(pixel, 1, 3, f);
    }
    fwrite(pad, 1, rowSize - width * 3, f);
  }

  fclose(f);
  return 0;
}
